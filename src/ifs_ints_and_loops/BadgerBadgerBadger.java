package ifs_ints_and_loops;

public class BadgerBadgerBadger {
	public static void main(String[] args) {
		for (int i = 0; i < 50; i++) {
			for (int j = 0; j < 12; j++) {
				System.out.println("BADGER");
			}
			for (int j = 0; j < 2; j++) {
				System.out.println("MUSHROOM");
			}
			System.out.println("A snake, a snake\n" + "Snaaake! A snaaaake\n" + "Oooh, it's a snake");
		}
	}
}
